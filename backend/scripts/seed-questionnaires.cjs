async function main() {
  const reset = process.argv.includes("--reset");
  const app = await createStrapi(await compileStrapi()).load();
  app.log.level = "error";
  const Q = "api::questionnaire.questionnaire";
  const C = "api::criterion.criterion";
  try {
    const existing = await app.db.query(Q).count();
    if (existing > 0 && !reset) {
      console.log(`[seedQ] Уже есть ${existing} опросников. --reset для пересоздания. Выходим.`);
      return;
    }
    if (reset) {
      console.log("[seedQ] --reset: очищаю criteria и questionnaires…");
      await app.db.query(C).deleteMany({ where: {} });
      await app.db.query(Q).deleteMany({ where: {} });
    }
    for (const q of QUESTIONNAIRES) {
      const rec = await app.db.query(Q).create({
        data: {
          slug: q.slug,
          name: q.name,
          subjectType: q.subjectType,
          order: q.order,
          active: true,
        },
      });
      let i = 1;
      for (const text of q.criteria) {
        await app.db.query(C).create({
          data: { text, order: i++, questionnaire: rec.id },
        });
      }
      console.log(`[seedQ] ${q.name} (${q.subjectType}) — критериев: ${q.criteria.length}`);
    }
    console.log(`[seedQ] Готово. Опросников: ${QUESTIONNAIRES.length}`);
  } finally {
    await app.destroy();
  }
}
main().then(() => process.exit(0)).catch((e) => { console.error("[seedQ] ОШИБКА:", e); process.exit(1); });
